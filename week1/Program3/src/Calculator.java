public class Calculator{ //class for calculator implementation
	public int  add(int valueOne,int valueTwo){//function to add two values
		int ans=valueOne+valueTwo;//add two values and assign answer in ans variable
		return ans;//return add value to main
	}
	
	public int  sub(int valueOne,int valueTwo){//function to sub values
		int anssub=valueOne-valueTwo;//subtract two values and assign answer in ans variable
		return anssub;//return subtract value to main
	}
	
	public int mul(int valueOne,int valueTwo){//function to multiplication
		int ansmul=valueOne*valueTwo;//multiplication two values and assign answer in ans variable
		return ansmul;//return multiplication value to main
	}
	public int div(int valueOne,int valueTwo){//function to division
		int ansdiv=valueOne/valueTwo;//division two values and assign answer in ans variable
		return ansdiv;//return division vale to main
	}
	
	
	}
	
